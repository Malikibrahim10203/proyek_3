            <h1 class="judul"> Menu Diet Sehat untuk Program Menurunkan Berat Badan </h1>
            <div class="group-text">
                <p class="p1">Rekomendasi Menu Diet untuk Menurunkan Berat Badan</p>
                <p class="p2">Pada dasarnya, perencanaan menu diet untuk menurunkan berat badan tidak hanya mengurangi porsi makanan saja, <br> namun juga perlu disesuaikan dengan kebutuhan nutrisi tubuh masing-masing individu. <br> Karena itu, berikut adalah rekomendasi menu diet seminggu untuk menurunkan berat badan yang bisa diterapkan.</p>
                
                <div class="p3">1.Senin</div>
                <p class="p4">Pada hari pertama, sebaiknya pilihlah menu makanan untuk diet yang dapat membantu memenuhi kebutuhan nutrisi serta <br>
                              memberikan efek kenyang lebih lama. Berikut rekomendasinya. <br> <br>
                            - Sarapan: 2 porsi telur rebus, oatmeal, dan buah alpukat. <br>
                            - Camilan (pukul 10–11 siang): 3 buah biskuit gandum dengan tambahan selai kacang. <br>
                            - Makan siang: 1 porsi salad sayur, daging dada ayam panggang, dan 2 buah ubi rebus. <br>
                            - Makan malam: 1 porsi brokoli rebus, jagung rebus, dan 1 gelas smoothie buah.
                </p> <br>

                <div class="p5">2.Selasa</div>
                <p class="p6">Pada hari kedua, disarankan untuk mengombinasikan menu makanan yang mengandung gizi lengkap, seperti karbohidrat <br> 
                            memberikan efek kenyang lebih lama. Berikut rekomendasinya. <br> <br>
                            - Sarapan: 2 buah tomat segar, buah sawo potong, 2 buah ubi jalar rebus, dan 1 porsi telur dadar. <br>
                            - Camilan (pukul 10–11 siang): 1 gelas susu almond dan 1 buah pisang. <br>
                            - Makan siang: 1 porsi daging dada ayam panggang, kentang rebus, dan sayur bayam bening. <br>
                            - Makan malam: 1 mangkuk smoothies mangga dan pisang.
                </p> <br>

                <div class="p7">3.Rabu</div>
                <p class="p8">Setelah memasuki hari ketiga, cobalah untuk membuat beberapa olahan makanan sehat berikut ini. <br> <br>
                            - Sarapan: 1 gelas susu rendah lemak, brokoli rebus, dan ubi jalar rebus.<br>
                            - Camilan (pukul 10–11 siang): 1 mangkuk yoghurt dan kacang almond. <br>
                            - Makan siang: 1 porsi mashed potato, sayur bayam bening, dan daging ayam panggang. <br>
                            - Makan malam: 1 porsi daging ikan panggang dan pakcoy rebus.
                </p> <br>

                <div class="p9">4.Kamis</div>
                <p class="p10">Setelah beberapa hari menjalani diet, tak jarang dari individu yang merasa bosan dengan menu makanan hariannya. Sebagai upaya mengantisipasi hal tersebut, cobalah untuk memilih bahan makanan sehat yang lebih beragam. <br> Misalnya, Anda bisa menggunakan nasi hitam atau nasi merah untuk dijadikan sebagai sumber karbohidrat pada menu diet hari keempat. Adapun rekomendasi menu makanan untuk diet sehat hari keempat adalah: <br> <br>
                            - Sarapan: 1 porsi oatmeal dan salad sayur.<br>
                            - Camilan (pukul 10–11 siang): 3 buah biskuit gandum dengan tambahan selai kacang. <br>
                            - Makan siang: 1 porsi sup ayam, nasi hitam/nasi merah, dan brokoli rebus. <br>
                            - Makan malam: 1 porsi greek yoghurt dan buah pepaya potong.
                </p> <br>

                <div class="p11">5.Jumat</div>
                <p class="p12">Selanjutnya, rekomendasi menu makanan sehat untuk diet pada hari kelima adalah sebagai berikut: <br> <br>
                            - Sarapan: 1 porsi roti gandum dengan isian selai kacang dan 1 gelas susu rendah lemak.<br>
                            - Camilan (pukul 10–11 siang): 1 gelas susu almond dan 1 buah alpukat. <br>
                            - Makan siang: 1 porsi mashed potato, daging dada ayam panggang, dan salad sayur. <br>
                            - Makan malam: 1 porsi steak tempe dan buah potong segar.
                </p> <br>

                <div class="p13">6.Sabtu</div>
                <p class="p14">Jika memiliki waktu senggang pada akhir pekan, cobalah untuk membuat olahan makanan yang lebih bervariasi. Berikut adalah beberapa rekomendasi menu untuk diet pada akhir pekan. <br> <br>
                            - Sarapan: 1 porsi sandwich roti gandum dengan isian selada, keju, tomat, dan telur rebus, 1 buah apel segar, dan 1 gelas susu.<br>
                            - Camilan (pukul 10–11 siang): 1 mangkuk yoghurt dengan topping buah segar. <br>
                            - Makan siang: 1 porsi kari kentang, daging dada ayam tumis, dan brokoli rebus. <br>
                            - Makan malam: 1 buah ubi jalar rebus dan 1 gelas buttermilk.
                </p> <br>

                <div class="p15">7.Minggu</div>
                <p class="p16">Adapun rekomendasi menu diet sehat untuk hari ketujuh yang bisa diterapkan adalah sebagai berikut <br> <br>
                            - Sarapan: 2 porsi telur rebus, roti gandum, dan 1 buah alpukat. <br>
                            - Camilan (pukul 10–11 siang): 1 gelas smoothie pisang. <br>
                            - Makan siang: ½ porsi nasi merah, daging ikan panggang, dan brokoli rebus.<br>
                            - Makan malam: 1 porsi daging dada ayam panggang, 1 buah tomat segar, dan wortel rebus. <br> <br>

                            Perlu dipahami, program diet yang sehat tidak cukup hanya dengan menerapkan pola makan seperti di atas. <br> Agar program diet dapat memberikan hasil yang optimal,
                            hal ini harus diimbangi dengan rutin berolahraga, istirahat yang cukup, serta mengelola stres dengan baik.
                </p> <br>
                
